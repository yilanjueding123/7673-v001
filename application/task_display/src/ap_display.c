INT32S  ap_display_queue_put(INT32U *queue, INT32U data)
{
	INT32U i;
	OS_CPU_SR cpu_sr;

	OS_ENTER_CRITICAL();

#if DISPLAY_BUF_NUM_MAX > 1

	if(data == 0)
	{	
		OS_EXIT_CRITICAL();
		return STATUS_FAIL;
	}

       #if TV_DET_ENABLE   // for tv out
	for(i=0; i<(DISPLAY_BUF_NUM_MAX - 1); i++) {
		if(data == queue[i])
		{
			OS_EXIT_CRITICAL();
			DBG_PRINT("DisPFAIL-1");
			return STATUS_FAIL;
		}
	}
	#endif   // end for tv out
	
#endif

	for (i=0; i<DISPLAY_BUF_NUM_MAX; i++) {
		if (!queue[i]) {
			queue[i] = data;

			OS_EXIT_CRITICAL();
			return STATUS_OK;
		}
	}

	OS_EXIT_CRITICAL();
	
	#if TV_DET_ENABLE   // for tv out
	DBG_PRINT("DisPFAIL-2");
	return STATUS_FAIL;
	#else
	return STATUS_OK;
	#endif   // end for tv out
}


